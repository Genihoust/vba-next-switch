int uiGetIndex(int x, int y, u16 fbWidth, u16 fbHeight) { return (x + y * fbWidth) * 4; }

void uiFill(u8* fb, u16 fbWidth, u16 fbHeight, int x, int y, int width, int height, u8 r, u8 g, u8 b) {
	if (fb == NULL) {
		return;
	}

	if (x + width < 0 || y + height < 0 || x >= fbWidth || y >= fbHeight) {
		return;
	}

	if (x < 0) {
		width += x;
		x = 0;
	}

	if (y < 0) {
		height += y;
		y = 0;
	}

	if (x + width >= fbWidth) {
		width = fbWidth - x;
	}

	if (y + height >= fbHeight) {
		height = fbHeight - y;
	}

	u8 colorLine[width * 4];
	for (int ly = 0; ly < width; ly++) {
		colorLine[ly * 4 + 0] = r;
		colorLine[ly * 4 + 1] = g;
		colorLine[ly * 4 + 2] = b;
		colorLine[ly * 4 + 3] = 255;
	}

	u8* fbAddr = fb + uiGetIndex(x, y, fbWidth, fbHeight);  // - (width * 4);
	for (int dx = 0; dx < height; dx++) {
		memcpy(fbAddr, colorLine, (size_t)(width * 4));
		fbAddr += fbWidth * 4;
	}
}

void uiDrawChar(u8* fb, u16 fbWidth, u16 fbHeight, char c, int x, int y, u8 r, u8 g, u8 b) {
	if (fb == NULL) {
		return;
	}

	unsigned char* data = asciiData[(int)c];
	for (int cy = 0; cy < 8; cy++) {
		if (y + cy < 0 || y + cy >= fbHeight) {
			continue;
		}

		unsigned char l = data[cy];
		for (int cx = 0; cx < 8; cx++) {
			if (x + cx < 0 || x + cx >= fbWidth) {
				continue;
			}

			if ((0b10000000 >> cx) & l) {
				u8* ptr = &fb[uiGetIndex(x + cx, y + cy, fbWidth, fbHeight)];
				*(ptr + 0) = r;
				*(ptr + 1) = g;
				*(ptr + 2) = b;
				*(ptr + 3) = 255;
			}
		}
	}
}

void uiDrawString(u8* fb, u16 fbWidth, u16 fbHeight, const char* string, int x, int y, u8 r, u8 g, u8 b) {
	if (fb == NULL) {
		return;
	}

	int len = (int)strlen(string);
	int cx = x;
	int cy = y;
	for (int i = 0; i < len; i++) {
		char c = string[i];
		if (c == '\n') {
			cx = x;
			cy += 8;
		}

		if (cx > fbHeight - 8) {
			break;
		}

		uiDrawChar(fb, fbWidth, fbHeight, c, cx, cy, r, g, b);
		cx += 8;
	}
}

#define FILENAMEBUFFER_SIZE (1024 * 8)  // 8kb

static UIState uiState = stateFileselect;
static char* filenameBuffer = NULL;
static char* filenames[256];
static int filenamesCount = 0;

static char statusMessage[1024];
static int statusMessageFadeout = 0;

static char selectedPath[PATH_LENGTH] = {'\0'};
static char currentDirectory[PATH_LENGTH] = {'\0'};
static int cursor = 0;
static int scroll = 0;

void uiStatusMsg(const char* format, ...) {
	statusMessageFadeout = 240;
	va_list args;
	va_start(args, format);
	vsprintf(statusMessage, format, args);
	va_end(args);

	printf("Status message %s\n", statusMessage);
}

static void enterDirectory() {
	getDirectoryContents(filenameBuffer, &filenames[0], &filenamesCount, currentDirectory, "gba");
	cursor = 0;
	scroll = 0;
}

static void enterPauseMenu() {
	filenamesCount = 0;
	char* nextFilename = filenameBuffer;
	addString(filenameBuffer, filenames, &filenamesCount, &nextFilename, "Continue");
	addString(filenameBuffer, filenames, &filenamesCount, &nextFilename, "Load Savestate");
	addString(filenameBuffer, filenames, &filenamesCount, &nextFilename, "Write Savestate");
	addString(filenameBuffer, filenames, &filenamesCount, &nextFilename, "Increase Frameskip");
	addString(filenameBuffer, filenames, &filenamesCount, &nextFilename, "Exit");
	cursor = 0;
	scroll = 0;
}

void uiInit() {
	filenameBuffer = (char*)malloc(FILENAMEBUFFER_SIZE);
	strcpy(currentDirectory, "");
	enterDirectory();
}

void uiDeinit() { free(filenameBuffer); }

void uiGetSelectedFile(char* out) { strcpy(out, selectedPath); }

UIResult uiLoop(u8* fb, u32 fbWidth, u32 fbHeight, u32 keysDown) {
	if (statusMessageFadeout > 0) {
		int fadeout = statusMessageFadeout > 255 ? 255 : statusMessageFadeout;
		uiDrawString(fb, fbWidth, fbHeight, statusMessage, 20, fbHeight - 40, fadeout, fadeout, fadeout);
		statusMessageFadeout -= 4;
	}

	if (uiState == stateFileselect || uiState == statePaused) {
		if (keysDown & KEY_X) return resultExit;

		if (keysDown & KEY_A) {
			if (uiState == stateFileselect) {
				char path[PATH_LENGTH] = {'\0'};

				if (!strcmp(filenames[cursor], "..")) {
					int length = strlen(currentDirectory);
					for (int i = length - 1; i >= 0; i--) {
						if (currentDirectory[i] == '/') {
							strncpy(path, currentDirectory, i);
							path[i] = '\0';
							break;
						}
					}
				} else
					snprintf(path, PATH_LENGTH, "%s/%s", currentDirectory, filenames[cursor]);

				if (isDirectory(path)) {
					strcpy(currentDirectory, path);
					enterDirectory();
				} else {
					strcpy(selectedPath, path);
					return resultSelectedFile;
				}
			} else {
				switch (cursor) {
					case 0:
						return resultUnpause;
					case 1:
						return resultLoadState;
					case 2:
						return resultSaveState;
					case 3:
						return resultIncFrameskip;
					case 4:
						return resultClose;
				}
			}
		}

		int scrollAmount = 0;
		if (keysDown & KEY_DOWN) scrollAmount = 1;
		if (keysDown & KEY_UP) scrollAmount = -1;
		if (keysDown & KEY_LEFT) scrollAmount = -5;
		if (keysDown & KEY_RIGHT) scrollAmount = 5;

		if (scrollAmount > 0) {
			for (int i = 0; i < scrollAmount; i++)
				if (cursor < filenamesCount - 1) {
					cursor++;
					if (cursor - scroll >= 60) {
						scroll++;
					}
				}
		} else if (scrollAmount < 0) {
			for (int i = 0; i < -scrollAmount; i++)
				if (cursor > 0) {
					cursor--;
					if (cursor - scroll < 0) {
						scroll--;
					}
				}
		}

		int i = 0;
		for (int j = scroll; j < filenamesCount; j++) {
			u8 color = 255;
			if (i + scroll == cursor) {
				uiFill(fb, fbWidth, fbHeight, 0, i * 12, fbWidth / 2, 8, 255, 255, 255);
				color = 0;
			}

			uiDrawString(fb, fbWidth, fbHeight, filenames[j], 0, i * 12, color, color, color);
			i++;
			if (i >= 60) {
				break;
			}
		}

		uiDrawString(fb, fbWidth, fbHeight, currentDirectory, 4, fbHeight - 12, 255, 255, 255);
	}
	return resultNone;
}

void uiSetState(UIState state) {
	uiState = state;
	if (state == statePaused)
		enterPauseMenu();
	else if (state == stateFileselect)
		enterDirectory();
}
